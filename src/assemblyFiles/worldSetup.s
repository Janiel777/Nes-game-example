;--Load attribute table---------;
LoadAttr:                       ;
    lda PPUSTATUS               ;
    LDA #$23                    ;
    STA $2006                   ;
    LDA #$c0                    ;
    STA $2006                   ;
    LDX #$00                    ;
LoadAttrLoop:                   ;
    LDA attributeTable0, x      ;
    STA $2007                   ;
    INX                         ;
    CPX #$40                    ;
    BNE LoadAttrLoop            ;
    rts                         ;
;-------------------------------;

;--Deafault Palettes----------------;
load_default_palettes:              ;
  lda PPUSTATUS                     ;
  lda #$3f                          ;
  sta PPUADDR                       ;
  lda #$00                          ;
  sta PPUADDR                       ;
  ldx #$00                          ;
                                    ;
@load_default_palettes_loop:        ;
  lda defaultPalettes, x            ;
  sta PPUDATA                       ;
  inx                               ;
  cpx #$20                          ;
  bne @load_default_palettes_loop   ;
  rts                               ;
;-----------------------------------;


